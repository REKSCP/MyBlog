package com.gugu.pojo;

public class GgNote {
	private int noteId;
	private String noteContent;
	private String noteDate;
	private int noteLike;
	private String notePhoto;
	public int getNoteId() {
		return noteId;
	}
	public void setNoteId(int noteId) {
		this.noteId = noteId;
	}
	public String getNoteContent() {
		return noteContent;
	}
	public void setNoteContent(String noteContent) {
		this.noteContent = noteContent;
	}
	public String getNoteDate() {
		return noteDate;
	}
	public void setNoteDate(String noteDate) {
		this.noteDate = noteDate;
	}
	public int getNoteLike() {
		return noteLike;
	}
	public void setNoteLike(int noteLike) {
		this.noteLike = noteLike;
	}
	public String getNotePhoto() {
		return notePhoto;
	}
	public void setNotePhoto(String notePhoto) {
		this.notePhoto = notePhoto;
	}
	

}

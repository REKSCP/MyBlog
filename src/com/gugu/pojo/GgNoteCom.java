package com.gugu.pojo;

public class GgNoteCom {
	private int noteId;
	private int noteComId;
	private String noteComContent;
	private String noteComDate;
	private String noteComName;
	public int getNoteId() {
		return noteId;
	}
	public void setNoteId(int noteId) {
		this.noteId = noteId;
	}
	public int getNoteComId() {
		return noteComId;
	}
	public void setNoteComId(int noteComId) {
		this.noteComId = noteComId;
	}
	public String getNoteComContent() {
		return noteComContent;
	}
	public void setNoteComContent(String noteComContent) {
		this.noteComContent = noteComContent;
	}
	public String getNoteComDate() {
		return noteComDate;
	}
	public void setNoteComDate(String noteComDate) {
		this.noteComDate = noteComDate;
	}
	public String getNoteComName() {
		return noteComName;
	}
	public void setNoteComName(String noteComName) {
		this.noteComName = noteComName;
	}

}
